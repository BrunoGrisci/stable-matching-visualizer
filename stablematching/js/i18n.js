  function format(text, params) {
    return String(text).replace(/\{(\w+)\}/g, (_, key) => {
      if (params && Object.prototype.hasOwnProperty.call(params, key)) {
        return String(params[key]);
      }
      return `{${key}}`;
    });
  }

